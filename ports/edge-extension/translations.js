// Get supported language codes
const supportedLanguages = Object.keys(translations);

// Apply translations to the UI
function applyTranslations(locale) {
  const t = translations[locale] || translations.en;
  
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (t[key]) {
      if (element.tagName === 'INPUT' && element.type !== 'radio') {
        element.placeholder = t[key];
      } else {
        element.textContent = t[key];
      }
    }
  });
  
  // Update placeholders separately
  document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
    const key = element.getAttribute('data-i18n-placeholder');
    if (t[key]) {
      element.placeholder = t[key];
    }
  });
  
  // Update loading text
  const loadingEl = document.getElementById('loading');
  if (loadingEl && t.scanning) {
    const textNode = Array.from(loadingEl.childNodes).find(node => node.nodeType === Node.TEXT_NODE);
    if (textNode) {
      textNode.textContent = t.scanning;
    }
  }
}

// Get current locale from storage or browser
function getCurrentLocale() {
  return new Promise((resolve) => {
    chrome.storage.sync.get(['locale'], (result) => {
      const locale = result.locale || navigator.language.split('-')[0] || 'en';
      resolve(supportedLanguages.includes(locale) ? locale : 'en');
    });
  });
}

// Save locale to storage
function saveLocale(locale) {
  return new Promise((resolve) => {
    chrome.storage.sync.set({ locale }, () => {
      resolve();
    });
  });
}
